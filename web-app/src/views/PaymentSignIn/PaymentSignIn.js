const PaymentSignIn = props => {
  const { history } = props;

  const classes = useStyles();

  const [formState, setFormState] = useState({
    isValid: false,
    values: {},
    touched: {},
    errors: {}
  });

  useEffect(() => {
    const errors = validate(formState.values, schema);

    setFormState(formState => ({
      ...formState,
      isValid: errors ? false : true,
      errors: errors || {}
    }));
  }, [formState.values]);

  const [open, setOpen] = React.useState(false);
  const [eOpen, setEOpen] = React.useState(false);
  const handleClick = () => {
    setOpen(true);
  };

  const handleClose = (event, reason) => {
    if (reason === 'clickaway') {
      return;
    }

    setOpen(false);
    setEOpen(false);
  };
  const handleBack = () => {
    history.goBack();
  };

  const handleChange = event => {
    event.persist();

    setFormState(formState => ({
      ...formState,
      values: {
        ...formState.values,
        [event.target.name]:
          event.target.type === 'checkbox'
            ? event.target.checked
            : event.target.value
      },
      touched: {
        ...formState.touched,
        [event.target.name]: true
      }
    }));
  };

  const handlePaymentSignIn = event => {
    event.preventDefault();
    //history.push('/');
    formState.values.password = sha1(formState.values.password);
    console.log(formState.values);
    axios.post("http://localhost:8000/api/v1/users/login/", formState.values)
    .then(function(response){
      setOpen(true)
      console.log(response);
      //localStorage.setItem("token",response.data.token);
      //localStorage.setItem("username",response.data.user.username);
      //localStorage.setItem("usertype",formState.values.user_type);
      setTimeout(function() { history.push("/payment/addlandlord"); }, 2000);
      
    })
    .catch(function (response) {
      //handle error
      console.log(response);
      setEOpen(true);
    });

  };

  const hasError = field =>
    formState.touched[field] && formState.errors[field] ? true : false;

  return (
    <div className={classes.root}>
      <Grid
        className={classes.grid}
        container
      >
        <Grid
          className={classes.quoteContainer}
          item
          lg={5}
        >
          <div className={classes.quote}>
            <div className={classes.quoteInner}>
            </div>
          </div>
        </Grid>
        <Grid
          className={classes.content}
          item
          lg={7}
          xs={12}
        >
          <div className={classes.content}>
            <div className={classes.contentHeader}>
              <IconButton onClick={handleBack}>
                <ArrowBackIcon />
              </IconButton>
            </div>
            <div className={classes.contentBody}>
              <form
                className={classes.form}
                onSubmit={handlePaymentSignIn}
              >
                <Typography
                  className={classes.title}
                  variant="h2"
                >
                  Sign in
                </Typography>
                <Typography
                  align="center"
                  className={classes.sugestion}
                  color="textSecondary"
                  variant="body1"
                >
                  Sign In to Access your Dashboard
                </Typography>
                <TextField
                  className={classes.textField}
                  error={hasError('username')}
                  fullWidth
                  helperText={
                    hasError('username') ? formState.errors.username[0] : null
                  }
                  label="Username"
                  name="username"
                  onChange={handleChange}
                  type="text"
                  value={formState.values.username || ''}
                  variant="outlined"
                />
                <TextField
                  className={classes.textField}
                  error={hasError('password')}
                  fullWidth
                  helperText={
                    hasError('password') ? formState.errors.password[0] : null
                  }
                  label="Password"
                  name="password"
                  onChange={handleChange}
                  type="password"
                  value={formState.values.password || ''}
                  variant="outlined"
                />
                <Button
                  className={classes.PaymentSignInButton}
                  color="primary"
                  disabled={!formState.isValid}
                  fullWidth
                  size="large"
                  type="submit"
                  variant="contained"
                >
                  Sign in now
                </Button>
                <Typography
                  color="textSecondary"
                  variant="body1"
                >
                  Don't have an account?{' '}
                  <Link
                    component={RouterLink}
                    to="/Buyer/sign-up"
                    variant="h6"
                  >
                    Sign up
                  </Link>
                </Typography>
              </form>
              <Snackbar
                  anchorOrigin={{vertical: 'bottom',horizontal: 'right'}}
                  open={eOpen}
                  autoHideDuration={6000}
                  onClose={handleClose}
                >
                <MySnackbarContentWrapper
                  variant="error"
                  className={classes.margin}
                  message="Username or Password might be wrong!"
                />
              </Snackbar>
              <Snackbar
                  anchorOrigin={{vertical: 'bottom',horizontal: 'right'}}
                  open={open}
                  autoHideDuration={6000}
                  onClose={handleClose}
                >
                <MySnackbarContentWrapper
                  variant="success"
                  className={classes.margin}
                  message="Successfully Logged In"
                />
              </Snackbar>
            </div>
          </div>
        </Grid>
      </Grid>
    </div>
  );
};

PaymentSignIn.propTypes = {
  history: PropTypes.object
};

